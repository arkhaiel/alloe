## Article 2: Mentions légales et données personnelles
L'édition et la maintenance du site sont assurées par MN.  
L'hébergeur des donnée statiques du site est [NETLIFY](https://netlify.com).  
Les données relatives aux utilisateurs sont stockées via le service [SUPABASE](https://supabase.com) sur un cloud AWS localisé 'eu-west-2'. Les données suivantes y sont stockées :  
- adresse email d'inscription
- textes écrits et enregistrés sur le site par les utilisateurs
- informations renseignées par l'utilisateur pour la complétion de son profil public
Les adresse email des membres sont également enregistrées sur la plateforme [MAILJET](https://mailjet.com), pour permettre l'envoi de mails d'information relatifs aux différentes campagnes qui seront créées sur le site. Les utilisateurs ne recevront jamais de mail à vocation commerciale, et leurs données personnelles ne seront communiquées à personne.

L'ensemble des données des membres sont sécurisées par une authentification à deux facteurs ("2FA").

